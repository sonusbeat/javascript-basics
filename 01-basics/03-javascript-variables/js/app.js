// Variable Names Rules
// - Cannot be a reserved keyword
// - Should be meaningful
// - Cannot start with a number (1name)
// - Cannot contain a space ( ) or hyphen (-)
// - Are case-sensitive, firstName and FirstName are different
// - You can declare multiple variables in one line
//   let firstName, lastName;

// Common Notations
// - Camel Case, (variableName)
// - Underscore, (variable_name)

// Var variable global scope
var name = "Daniel";

// Let varible block scope
let lastName = "Gonzalez";

console.log('First Name: ', name);
console.log('Last Name: ', lastName);