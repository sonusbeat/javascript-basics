/* Javascript Comparison Operators */

var x = 1;

// Relational Operators
console.log('x is bigger than 0:', x > 0);
console.log('x is bigger than or equal to 0:', x >= 1);
console.log('x is less than 1:', x < 1);
console.log('x is less than or equal to 1:', x <= 1);
console.log('-'.repeat(35));

// Equality Operators
console.log('x is equal to 1:', x === 1);
// === means is equal by type and value
console.log('x is exactly equals to 1:', x === '1');

// ! NOT Operator
console.log('x is not equal to 0:', x != 0);
