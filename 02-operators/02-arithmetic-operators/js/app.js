/* Javascript Arithmetic Operators */

var x = 10;
var y = 4;

console.log('Addition:', x + y);
console.log('Subtraction:', x - y);
console.log('Multiplication:', x * y);
console.log('Division:', x / y);
console.log('Remainder of division:', x % y);
console.log('Exponentiation:', x ** y);

var i = 0;

// Increment Operator (++)
i = i + 1;
console.log('i:', i);

i += 1;
console.log('i:', i);

i++;
console.log('i:', i);

console.log('-'.repeat(35));

i = 0;

console.log('i:', i++);
console.log('i:', i++);

console.log('-'.repeat(35));

i = 0;

console.log('i:', ++i);
console.log('i:', ++i);

console.log('-'.repeat(35));

// Decrement Operator (--)

i = 10;

console.log('i:', i--);
console.log('i:', i--);

console.log('-'.repeat(35));

i = 10;

console.log('i:', --i);
console.log('i:', --i);